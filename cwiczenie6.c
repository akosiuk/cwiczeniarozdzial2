#include <stdio.h>

int main() 
{
    // Deklaracja i przypisanie wartosci do zmiennej 'liczba'
    int liczba = 10;

    // Obliczenia
    int podwojona = 2 * liczba;
    int kwadrat = liczba * liczba;

    // Wyswietlanie wyników
    printf("Liczba: %d\n", liczba);
    printf("Podwojona wartość: %d\n", podwojona);
    printf("Kwadrat liczby: %d\n", kwadrat);

    return 0;
}